        đó.</p>
    <p style="text-align:justify">Theo điều 112 Bộ luật Lao động năm 2019 mới được sửa đổi, bổ sung và có hiệu lực từ
        ngày 01/01/2021, kể từ <strong>lịch nghỉ lễ 2021</strong> trở đi, người lao động trên cả nước sẽ có thêm 1 ngày
        nghỉ lễ Quốc Khánh (nghỉ ngày 2/9 và 1 ngày liên kề trước hoặc sau do Thủ tướng quyết định).</p>
